<h3>What is the Blue Noodle Challenge? - Kid's Portal</h3>
<p>Every six weeks Blue Noodle provides a different learning challenge and cross curriculum lesson that teachers or parents can incorporate in the classroom or at home. The challenge begins on the kid's portal page. On this page, the learners can begin their journey watching videos or a presentation about the chosen topic. Next the learner can visit each subject matter page to engage in a related activity. Look for the portal marked "Blue Noodle Challenge per subject portal. For example writing will have a writing activity related to the topic, spelling will contain words related to the chosen topic and so on. The challenge also presents ways that teacher or parents can extend learning outside the computer experience.</p>
</div>
